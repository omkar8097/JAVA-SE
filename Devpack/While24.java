package Devpack;

public class While24 {
	
	public void setWhile()
	{
		int i=3;
		while(i!=30)
		{
			if(i==24)
			{
				i++;
			}
			else
			{
			System.out.println(i);
			i++;
			}
		}
	}
	public static void main(String[] args) {
		While24 w = new While24();
		w.setWhile();
	}

}
